function openmediabox(){
    if($('#show_gallery .openbox').is(':visible')){
        $('#show_gallery .openbox').hide();
    }else{
        $('#show_gallery .openbox').show();
    }
}
function openchatbox(){
    if($('#show_chat .openbox').is(':visible')){
        $('#show_chat .openbox').hide();
    }else{
        $('#show_chat .openbox').show();
    }
}